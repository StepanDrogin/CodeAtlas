export const isCodeAtlasLocale = (value: unknown): value is CodeAtlasLocale =>
  typeof value === 'string' && CODEATLAS_LOCALES.some((locale) => locale.code === value)

export const isCodeAtlasThemePreference = (value: unknown): value is CodeAtlasThemePreference =>
  typeof value === 'string' && CODEATLAS_THEMES.some((theme) => theme.code === value)

export const translateCodeAtlas = (
  locale: CodeAtlasLocale,
  key: CodeAtlasMessageKey,
  params: Record<string, string | number> = {}
) => {
  const template = (locale === 'en' ? englishMessages[key] : localizedMessages[locale]?.[key]) ?? englishMessages[key]

  return Object.entries(params).reduce(
    (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
    template
  )
}
